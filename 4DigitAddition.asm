.model small
.stack 100H
.data
data1 dw 00H
msg db 10,13,"Enter num 1: $"
msg1 db 10,13,"Enter num 2: $"
msg2 db 10,13,"Sum: $"
.code
.startup
MOV BX, 0000
MOV AH,09
MOV DX,OFFSET msg
INT 21H
MOV CX, 4
AGAIN: MOV AH, 01
INT 21H
CMP AL, 'A'
JGE P1
SUB AL,30H
JMP P4
P1: SUB AL, 37H
P4: SHL BX, 4
MOV AH,00
ADD BX, AX
LOOP AGAIN
MOV data1, BX
MOV AH,09
MOV DX,OFFSET msg1
INT 21H
MOV CX, 4
AGAIN2: MOV AH, 01
INT 21H
CMP AL, 'A'
JGE P2
SUB AL,30H
JMP P3
P2: SUB AL, 37H
P3: SHL BX, 4
MOV AH,00
ADD BX,AX
LOOP AGAIN2
ADD BX, data1
MOV AH,09
MOV DX,OFFSET msg2
INT 21H
MOV DX, 00
MOV CX, 4
AGAIN3: ROL BX, 4
MOV DX,BX
AND DX, 0FH
CMP DX, 09
JG L6
ADD DX,30H
JMP L7
L6: ADD DX, 37H
L7: MOV AH,02
INT 21H
LOOP AGAIN3
MOV AH, 4CH
INT 21H
.exit
end