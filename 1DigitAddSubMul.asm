.model small
.stack 100H
.data
msg db 10,13,"Enter the first no.:: $"
msg1 db 10,13,"Enter the second no.:: $"
msg2 db 10,13,"1: ADD$"
msg3 db 10,13,"2: SUB$"
msg4 db 10,13,"3: MUL$"
msg5 db 10,13,"Result is :: $"
msg6 db 10,13,"Enter your choice::$"
.code
.startup
MOV AH,09
MOV DX,OFFSET msg2
INT 21H
MOV AH,09
MOV DX,OFFSET msg3
INT 21H
MOV AH,09
MOV DX,OFFSET msg4
INT 21H
MOV AH,09
MOV DX,OFFSET msg6
INT 21H
MOV AH, 01
INT 21H
CMP AL, '1'
JE LBADD
CMP AL, '2'
JE LBSUB
JMP LBMUL
LBADD:
MOV AH,09
MOV DX,OFFSET msg
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
MOV BL, AL
MOV AH,09
MOV DX,OFFSET msg1
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
ADD BL,AL
MOV AH,09
MOV DX,OFFSET msg5
INT 21H
MOV DL,BL
CMP DL, 09
JG L1
ADD DL,30H
JMP L2
L1: ADD DL, 37H
L2: MOV AH,02
INT 21H
JMP LBEXIT
LBSUB:
MOV AH,09
MOV DX,OFFSET msg
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
MOV BL, AL
MOV AH,09
MOV DX,OFFSET msg1
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
cmp BL, AL
JG B
SUB AL, BL
MOV BL, AL
jmp C
B: SUB BL, AL
C: MOV AH,09
MOV DX,OFFSET msg5
INT 21H
MOV DL,BL
CMP DL, 09
JG L3
ADD DL,30H
JMP L4
L3: ADD DL, 37H
L4: MOV AH,02
INT 21H
JMP LBEXIT
LBMUL:
MOV AH,09
MOV DX,OFFSET msg
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
MOV BL, AL
MOV AH,09
MOV DX,OFFSET msg1
INT 21H
MOV AH, 01
INT 21H
SUB AL,30H
MUL BL
MOV AH,09
MOV DX,OFFSET msg5
INT 21H
MOV DL,AL
CMP DL, 09
JG L5
ADD DL,30H
JMP L6
L5: ADD DL, 37H
L6: MOV AH,02
INT 21H
JMP LBEXIT
LBEXIT:
MOV AH, 4CH
INT 21H
.exit
end